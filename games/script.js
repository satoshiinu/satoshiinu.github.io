function main() {
    //キャンバスの初期化
    ctx.clearRect(0, 0, 1280, 720)

    //キー
    addEventListener("keyup", keyupfunc, false);
    addEventListener("keydown", keydownfunc, false);

    //移動
    if (player.xsp > 0) {
        player.xsp = Math.floor(player.xsp * 0.85 * 1000) / 1000;
    } else {
        player.xsp = Math.ceil(player.xsp * 0.85 * 1000) / 1000;
    }
    if (player.ysp > 0) {
        player.ysp = Math.floor(player.ysp * 0.85 * 1000) / 1000;
    } else {
        player.ysp = Math.ceil(player.ysp * 0.85 * 1000) / 1000;
    }

    if (key.up) player.ysp -= 0.5;
    if (key.down) player.ysp += 0.5;
    if (key.right) player.xsp += 0.5;
    if (key.left) player.xsp -= 0.5;

    if (player.xsp > 0) {
        player.x += Math.floor(player.xsp);
    } else {
        player.x += Math.ceil(player.xsp);
    }
    if (player.ysp > 0) {
        player.y += Math.floor(player.ysp);
    } else {
        player.y += Math.ceil(player.ysp);
    }

    for (var y = 0; y < 13; y++) {
        for (var x = 0; x < 21; x++) {
            if (map[y + Math.floor(player.y / 16)][x + Math.floor(player.x / 16)] == 0) ctx.drawImage(img.tile, 0, 0, 16, 16, (x * 16 + (16 - player.x % 16) - 16) * zoom, (y * 16 + (16 - player.y % 16) - 16) * zoom, 16 * zoom, 16 * zoom);
        }
    }

    //プレイヤー描画
    ctx.drawImage(img.player, 0, 0, 16, 24, 160 * zoom, 90 * zoom, 16 * zoom, 24 * zoom);

    console.log("x:" + player.x + ",y:" + player.y + ",xsp:" + player.xsp + ",ysp:" + player.ysp)
    //ループ
    requestAnimationFrame(main);
}
requestAnimationFrame(main);

function keydownfunc(parameter) {
    var key_code = parameter.keyCode;
    if (key_code == 37) key.left = true;
    if (key_code == 38) key.up = true;
    if (key_code == 39) key.right = true;
    if (key_code == 40) key.down = true;
    parameter.preventDefault();
}

function keyupfunc(parameter) {
    var key_code = parameter.keyCode
    if (key_code == 37) key.left = false;
    if (key_code == 38) key.up = false;
    if (key_code == 39) key.right = false;
    if (key_code == 40) key.down = false;
}

